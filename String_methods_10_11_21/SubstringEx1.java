package String_methods_10_11_21;

public class SubstringEx1 {
public static void main(String[] args) {
	String s="Manonmani Pandukumar";    
	 System.out.println("Original String: " + s);  
	 System.out.println("Substring starting from index 6: " +s.substring(6));//  ani Pandukumar
	 System.out.println("Substring starting from index 0 to 6: "+s.substring(0,6)); // Manonm

}
}
