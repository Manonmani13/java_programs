package StringMethods;

public class CharAt3 {
public static void main(String[] args) {
String s="hi i am manonmani";
int st=s.length();
System.out.println("Character at 0 index is: "+ s.charAt(0));
System.out.println("Character at last index is: "+ s.charAt(st-1));      

}
}
/*
Character at 0 index is: h
Character at last index is: i
*/