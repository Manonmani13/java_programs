package Map;

import java.util.HashMap;

public class HashMap1 {
public static void main(String[] args) {
	HashMap hm=new HashMap();
	hm.put("Tamil mark",90);
	hm.put("English mark",90);
	hm.put("Maths marks",80);
	hm.put("Science mark",100);
	hm.put("Soial mark",98);
	System.out.println(hm);
	
}
}
//{Soial mark=98, English mark=90, Science mark=100, Tamil mark=90, Maths marks=80}
