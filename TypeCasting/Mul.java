package TypeCasting;

public class Mul {
	public static void main(String[] args) {
	    int x = 5;
	    int y = 3;
	    System.out.println(x * y);
	    System.out.println(x / y);
	    System.out.println(x % y);
	    ++x;
	    System.out.println(x);
	    --y;
	    System.out.println(y);
	  }
}
