package Pattern_Pro;

public class Patt8 {
public static void main(String[] args) {
	 for(int j =5;j>=1;j--)
	    {
	        for(int i =1;i<=j;i++)
	        {
	            System.out.print(i+" ");
	        }
	    System.out.println();
	    }
	     
	    for(int k =1;k<=5;k++)
	    {
	     
	    for(int i =1;i<=k;i++)
	    {
	        System.out.print(i+" ");
	    }
	    System.out.println();
	    }
}
}
