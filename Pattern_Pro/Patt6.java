package Pattern_Pro;

public class Patt6 {
public static void main(String[] args) {
	 for (int j = 1; j<= 6; j++) 
     {
         for (int i = j; i >= 1; i--)
         {
             System.out.print(i+" ");
         }
           
         System.out.println();
     }
      
}
}
