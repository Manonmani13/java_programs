package Program_Practie_9_11_21;

public class Pattern_30 {
public static void main(String[] args) {
	for(int i=0;i<5;i++)
	{
		int k=65;
		for(int j=0;j<5;j++)
		{
			System.out.print((char)(j+k)+" ");
			k++;
		}
		System.out.println();
		
	}
}
}
