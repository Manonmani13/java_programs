public class Square {
	public static void main(String[] args) {
		int num=9;
		  int sqrt;
		  
		  for(int i=1;i<=num;i++)
		  {
		  	sqrt=i*i;
		    System.out.println("sqrt of"+" "+i+"="+sqrt);
		    
		  }

	}
}
